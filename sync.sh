#!/bin/bash
cd /Users/ulad/repos
echo "--- [18:53:05] Синхронизация памяти ---"

# 1. Забираем изменения из облака (на случай правок с другого устройства)
git pull origin main --rebase

# 2. Проверяем изменения локально
if [[ -n $(git status -s) ]]; then
    echo "Обнаружены новые данные. Сохраняю..."
    git add .
    git commit -m "AI Memory Update: $(date +'%Y-%m-%d %H:%M')"
    
    # 3. Пытаемся отправить
    if git push origin main; then
        echo "--- [УСПЕХ] Память синхронизирована с GitHub ---"
    else
        echo "--- [ОШИБКА] Не удалось отправить. Проверьте интернет или права доступа ---"
    fi
else
    echo "--- [ОК] Изменений нет. Локальная память совпадает с облаком ---"
fi
